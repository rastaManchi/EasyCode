feed = [2, 1, 3]

action = input('Введите команду\n1-покормить\n2-Информация\nstop-выйти\n')
while action != 'stop':
    if action == '2':
        for i in range(len(animals)):
            print(f'{animals[i]} {names[i]} сегодня покормили {feed[i]} раз(а)')
            print()
        # <животное1> <имя1> сегодня покормили <число1> раз(а)
